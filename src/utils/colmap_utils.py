def parse_colmap_pts(cam_extrinsics, points3D, transform=None):
    """
    Parse COLMAP points and correspondents.

    Input:
        @cam_extrinsics  Dictionary of images from COLMAP.
        @points3D        Dictionary of 3D points from COLMAP.
        @transform       3x3 matrix to transform xyz.
    Output:
        PointCloud with xyz, rgb, errors and correspondences.
    """
    xyz = []
    rgb = []
    err = []
    points_id = []

    for k, v in points3D.items():
        points_id.append(k)
        point_xyz = v.xyz
        if transform is not None:
            point_xyz = transform @ point_xyz
        xyz.append(point_xyz)
        rgb.append(v.rgb)
        err.append(v.error)

    xyz = np.array(xyz, dtype=np.float32)
    rgb = np.array(rgb, dtype=np.uint8)
    err = np.array(err, dtype=np.float32)
    points_id = np.array(points_id)

    if len(points_id) > 0:
        points_idmap = np.full([points_id.max()+2], -1, dtype=np.int64)
        points_idmap[points_id] = np.arange(len(xyz))
    else:
        points_idmap = np.array([], dtype=np.int64)

    corr = {}
    for image in cam_extrinsics.values():
        # Get valid point3D indices (those that are >= 0, meaning they have 3D points)
        valid_mask = image.point3D_ids >= 0
        idx = image.point3D_ids[valid_mask]
        if len(idx) > 0 and len(points_idmap) > 0:
            corr[image.name] = points_idmap[idx]
        else:
            corr[image.name] = np.array([], dtype=np.int64)

    return PointCloud(points=xyz, colors=rgb, errors=err, corr=corr)
